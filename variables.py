# Variables in python

x = input("Please enter x value? ")
y = input("Please enter y value? ")
x_value = float(x)
y_value = float(y)
print(f"Addition of x and y is {x_value + y_value}")
print(f"Subtraction of x and y is {x_value - y_value}")
print(f"Square of x and y is {x_value ** y_value}")
print(f"Multiplication of x and y is {x_value * y_value}")
print(f"Modular of x and y is {x_value % y_value}")
print(f"Division of x and y is {round(x_value / y_value, 2)}")
